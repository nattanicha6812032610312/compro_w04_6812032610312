#include <stdio.h> //แก้: ต้องมี # และ < >

int main() {       // int: Keyword, main: Identifier
    int a = 10;    //แก้: เพิ่ม ; ปิดท้าย/ int: Keyword, a: Identifier, 10:Constan
    printf("a = %d", a); //แก้: เพิ่ม ; ปิดท้าย / printf: Identifier, "a = %d": Cons
    return 0;  //แก้: เพิ่ม ; ปิดท้าย / return: Keyword, 0: Constant
}